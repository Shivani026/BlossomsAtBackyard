<hr style="border-width:20px; border-color:#64DC17"><div class="hr-container" style="top:-15px;left:41%"><p>THAI APPLE(BER)</p></div></hr>
<p style="font-weight:bold;font-size:18px;">Varieties</p>
Kaithali, Umran, Gola, Banarasi and Goma Keerthi are the cultivable varieties of ber.
<p style="font-weight:bold;font-size:18px;">Soil and climate</p>
The ber plant comes well under arid and semi-arid situation. It can tolerate salinity and alkalinity.
<p style="font-weight:bold;font-size:18px;">Planting material</p>
Budded plants are used for planting.
<p style="font-weight:bold;font-size:18px;">Planting</p>
Planting is done during July-August with a spacing of 7 x 7 m in pits of 60 cm x 60 cm x 60 cm size. For Kaithali, 8 x 3 m and for Banarasi 8 x 4 m spacing is followed.
<p style="font-weight:bold;font-size:18px;">Irrigation</p>
rrigate the plants initially for establishment. Provide 5 % slope towards the base of the tree for in situ water harvesting.
<p style="font-weight:bold;font-size:18px;">Manures and fertilizers (per plant/year)</p>
Manuring should be done immediately after pruning.<br>
Spray 2% KNO3 thrice at monthly intervals in January, February and March.<br>
<img src="ber1.png" style="height:250px;width:450px;position:relative;left:200px;top:10px;bottom:10px;border-radius:6px">
<p style="font-weight:bold;font-size:18px;">Training and pruning</p>
 Remove the root stock sprouts and have a straight stem upto 75 cm from the ground level. It is very important in the early years to build up a strong framework and in later years to maintain vigour to improve fruit size and quality.
During February – March, prune the trees and thin the crowded branches to provide maximum fruit bearing area in the tree.
<p style="font-weight:bold;font-size:18px;">Plant protection<br>Pests<br>Fruit fly</p>
Destroy all the infested fruits. Dig the soil under tree canopy to destroy pupae and incorporate 1.3% Lindane dust @ 30 g /tree.  Spray Malathion 50 EC or Chlorpyriphos 20 EC or Quinalphos 25 EC at 2 ml/lit. Use polythene bags fish meal trap with 5 gm of wet fish meal + 1 ml Dichlorvos in cotton. 50 traps are required/ha, fish meal and Dichlorvos soaked cotton are to be renewed once in 20 and 7 days respectively.
<p style="font-weight:bold;font-size:18px;">Scale insects</p>
During pruning, all the affected materials should be collected and burnt.  The trees should be sprayed with Phosphamidon 40 SL or Methyl demeton 25 EC @ 2 ml/lit.
<p style="font-weight:bold;font-size:18px;">Diseases<br>Black leaf spot</p>
Spray Carbendazim 1 g/lit or Chlorothalonil 2 g/lit at 15 days interval from the initial appearance of the symptom. 
<p style="font-weight:bold;font-size:18px;">Powdery mildew</p>
Spray  Dinocap 1 ml/ litre to control the disease.
<p style="font-weight:bold;font-size:18px;">Yield</p>
The yield ranges about 70-80 Kg of fruits/tree/year
<p style="font-weight:bold;font-size:18px;">Seed Technology:</p>
Seeds of Ber attain physiological maturity 13 weeks after anthesis.It is indicated by yellowish red colour of fruit pericarp.Stones can be size graded using 22/64" round perforated metal sieve. Ber stones can be stored upto 30 months without any treatment under ambient conditions.
