<img src="tom6.png" style="height:180px;width:230px;position:relative;left:250px;top:10px;bottom:10px;border-radius:6px">
<p style="position:relative;left:100px;font-size:18px"> &nbsp &nbsp &nbsp &nbsp Tomato grown in polyhouse &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp  &nbsp &nbsp Tomato at harvest stage</p>
<p style="font-size:15px">
<p style="font-weight:bold">Yield</p>
Varieties : 30 - 40 t / ha</br>
Hybrids  : 80 - 95 t / ha</br>
Boom flower - N spray at 2ml / litre in three sprays – 30 days, 55 days and 75 days after planting increase the yield.
<p style="font-weight:bold">Soil</p>
Well drained loamy soils rich in organic matter with a pH range of 6.5-7.5.
<p style="font-weight:bold">Season of sowing</p>
May - June and November – December
</p>
<img src="tom7.jpg" style="height:180px;width:230px;position:relative;left:150px;top:10px;bottom:10px;border-radius:6px">
<img src="tom8.png" style="height:180px;width:230px;position:relative;left:250px;top:10px;bottom:10px;border-radius:6px">
<p style="position:relative;left:100px;font-size:18px"> &nbsp &nbsp &nbsp &nbsp &nbsp Tomato growing soil &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp  &nbsp &nbsp &nbsp &nbsp  &nbsp &nbsp Main field preparation</p>
<p style="font-size:15px">
<p style="font-weight:bold">Field preparation</p>
Plough the land to fine tilth. Thoroughly prepare the field with the addition of FYM @ 25 t/ ha and form ridges and furrows at a spacing of 60 cm. Apply 2 kg/ha of Azospirillum and 2 kg/ha of Phosphobacteria by mixing with 50 kg of FYM. Irrigate the furrows and transplant 25 days old seedlings on the sides of ridges. Life irrigation to be given on 3rd day of planting.
<p style="font-weight:bold">Nursery bed preparation</p>
Apply FYM 10 kg, Neem cake 1 kg, VAM 50 g, enriched Superphosphate 100 g and Furadon 10 g per square meter before sowing.
<p style="font-weight:bold">Spacing for varieties</br>
PKM 1, Paiyur 1, COTH 2, TNAU Tomato Hybrid CO 3 : 60 x 45 cm</br>
CO 3 : 45 x 30 cm</p></br>
<p style="font-weight:bold">Mulching</p>
Mulch with black LDPE sheets of 25 micron thickness and bury both the ends into the soil to a depth of 10 cm
<p style="font-weight:bold">Weed control</p>
Apply Pendimethalin 1.0 kg a.i./ha or Fluchloralin 1.0 kg a.i / ha as pre-emergence herbicide, followed by hand weeding once at 30 days after planting.
<p style="font-weight:bold">Growth regulators</p>
Spray 1.25 ppm (625 ml in 500 litres of water) Triacontanol at 15 days after transplanting and at full bloom stage to increase the yield.
</p>
