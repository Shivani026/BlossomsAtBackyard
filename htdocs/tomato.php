<p style="font-size:15px">
<p style="font-weight:bold">Varieties</p>
PKM 1,CO.1 ,CO.2 ,CO.3 , (Marutham) and Paiyur 1
<p style="font-weight:bold">Hybrids</p>
COTH.1 ,COTH 2 and TNAU Tomato Hybrid CO 3
</p> 
<img src="tom10.png" style="height:380px;width:700px;position:relative;left:50px;top:20px;bottom:25px;border-radius:6px">
<p style="font-size:15px">
</br>
<p style="font-weight:bold">Seed rate</p>
Varieties : 300-350 g / ha</br>
Hybrids : 100-150 g / ha
<p style="font-weight:bold">Seed treatment</p>
Treat the seeds with Trichoderma viride 4 g or Pseudomonas fluorescens 10 g or Carbendazim 2 g per kg of seeds 24 hours before sowing. Just before sowing, treat the seeds with Azospirillum @ 40 g / 400 g of seeds. Sow in lines at 10 cm apart in raised nursery beds and cover with sand.
</p>
<img src="tom1.png" style="height:180px;width:230px;position:relative;left:150px;top:10px;bottom:10px;border-radius:6px">
<img src="tom2.png" style="height:180px;width:230px;position:relative;left:250px;top:10px;bottom:10px;border-radius:6px">
<p style="position:relative;left:100px;font-size:18px"> &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp Tomato Seed &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp Tomato protray nursery</p>
<p style="font-size:15px">
<p style="font-weight:bold">Protected nursery</p>
<ul>
<li>Prepare the nursery area of 3 cents with slanting slope of 2 % for the seedling production to cover 1 ha.</li>
<li>Cover the nursery area with 50 % shade net and cover the sides using 40/50 mesh insect proof nylon net.</li>
<li>Form raised beds of 1 m width and convenient length and place HDPV pipes at 2m interval for further protection with polythene sheets during rainy months.</li>
<li>Mix sterilized cocopeat @ 300 kg with 5 kg neem cake along with Azospirillum and Phosphobacteria each @ 1 kg. Approximately 1.2 kg of cocopeat is required for filling one protray. 238 protrays (98 cells) are required for the production of 23,334 seedlings, which are required for one hectare adopting a spacing of 90 x 60 x 60 cm in paired row system.</li>
<li>Sow the treated seed in protrays @ one seed per cell.</li>
<li>Cover the seed with cocopeat and keep the trays one above the other and cover with a polythene sheet till germination starts</li>
<li>After six days, place the protrays with germinated seeds individually on the raised beds inside the shade net</li>
<li>Water with rosecan everyday and drench with NPK 19:19:19 @ 0.5% (5g/l) at 18 days after sowing</li>
</ul>
<p style="font-weight:bold">Irrigation</p>
After establishment of seedlings, irrigate at weekly intervals.
<p style="font-weight:bold">Manuring</p>
<p style="font-weight:bold">Varieties</p>
Basal dose : FYM 25 t/ha, NPK 75:100:50 kg / ha</br>
Borax 10 kg and Zinc sulphate 50 kg / ha</br>
Top dressing : 75 kg N/ha on 30th day of planting or during earthing up.
<p style="font-weight:bold">Hybrids</p>
Basal dose : FYM 25 t/ha, NPK 50:250:100 kg/ha</br>
Borax 10 kg and Zinc sulphate 50 kg/ha</br>
Top dressing : N and K each 150 kg/ha in 3 equal splits at 30, 45 and 60 days after planting.
</p>
<img src="tom3.png" style="height:180px;width:230px;position:relative;left:150px;top:10px;bottom:10px;border-radius:6px">
<img src="tom4.jpg" style="height:180px;width:230px;position:relative;left:250px;top:10px;bottom:10px;border-radius:6px">
<p style="position:relative;left:100px;font-size:18px"> &nbsp  Surface irrigation in tomato field  &nbsp &nbsp &nbsp &nbsp &nbsp  &nbsp &nbsp Drip irrigated Tomato</p>
<p style="font-size:15px">
<p style="font-weight:bold">Layout and planting for drip irrigation & fertigation</p>
<ul>
<li>Apply FYM @ 25 t / ha as basal before last ploughing.</li>
<li>Apply 2 kg/ha of Azospirillum and 2 kg/ha Phosphobacteria by mixing with 50 kg of FYM.</li>
<li>Apply 75 % total recommended dose of superphosphate ie 1172 kg / ha as basal.</li>
<li>Install the drip irrigation with main and sub main pipes and place lateral tubes at an interval of 1.5 m</li>
<li>Place the drippers in lateral tubes at an interval of 60 cm and 50 cm spacing with 4 LPH and 3.5 LPH capacities respectively.</li>
<li>Form raised beds of 120 cm width at an interval of 30 cm and place the laterals at the centre of each bed.</li>
<li>Before planting, wet the beds using drip system for 8-12 hrs.</li>
<li>Planting to be done at a spacing of 90 x 60 x 60 cm in the paired row system, using ropes marked at 60 cm spacing.</li>
<li>Gap filling has to be done at 7th day after transplanting.</li>
</ul>
<p style="font-weight:bold">Protected cultivation</br>Production practices for cultivation of tomato under shade net</p>
During summer, the hybrid tomato can be grown in a shade level of 35 per cent under paired row planting system (80 x 40 x 60 cm - between pairs, rows and plants) with a basal application of 50 kg each of N and K and 250 kg of P / ha and fertigation of 200 kg each of N and K through straight fertilizers.
<p style="font-weight:bold">Protected cultivation of tomato in polyhouse</p>
During rainy season, the indeterminate tomato hybrid has to be grown in the medium consisting of FYM : composted coir pith: sand (2:1:1) with irrigation regime of 20kPa and basal application of 50kg each of NPK/ha as straight fertilizers and 250 kg each of NPK as water soluble and straight fertilizers through fertigation along with black polyethylene mulch (50 microns).
<p style="font-weight:bold">Duration</p>
110- 115 days from transplanting (135 - 140 days from sowing)
</p>
