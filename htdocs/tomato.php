<p style="position:relative;left:100px;font-size:18px">&nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp &nbsp Spacing of Tomato</p>
<p style="font-size:15px">
<p style="font-weight:bold">Training of hybrids</p>
<ul>
<li>Stake the plants 30 days after planting with 1 - 1.5 m tall stakes.</li>
<li>Remove the side branches up to 20 cm from ground level.
</li>
<p style="font-weight:bold">Micronutrient spray</p>
<li>Foliar spray of ZnSO4 @ 0.5 per cent thrice at 10 days interval from 40 days after planting.</li>
<li>Spray 19:19:19 + Mn @ 1 % at 60 days after planting.</li>
</ul>
</p>
<p style="font-size:20px">
<p style="font-weight:bold">Fertigation schedule for tomato hybrids</p>
