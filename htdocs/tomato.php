75% of RD of P applied as superphosphate as basal application= 1172 kg/ha</br>
1. 19:19:19 = 132 kg / ha</br>
2. 12:61:0 = 62 kg / ha</br>
3. 13:0:45 = 500 kg / ha</br>
4. Urea = 223 kg / ha</br>
</p>
