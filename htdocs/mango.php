<p style="font-size:15px">
<p style="font-weight:bold;position:relative;top:20px;">Field preparation</p>
Dig pits of 1 m x 1 m x 1 m. Fill in with topsoil mixed with 10 kg of FYM and 100 g per pit.
<p style="font-weight:bold;position:relative;top:20px;">Spacing</p>
Adopt any one of the following spacing depending on requirements.
<ol>
<li>Under conventional system of planting: 7-10 m either way</li>
<li>High Density Planting : 5m x 5 m  (400 plants /ha)</li>
<li>Double hedge row system : Adopt  a spacing of  5m x  5m within double rows and 10m between sucessive double rows (266 plants/ha)</li>
</ol>
<p style="font-weight:bold;position:relative;top:20px;">Canopy management:</p>
Remove root stock sprouts and low lying branches nearer to ground to facilitate easy cultural operations. Remove overlapping, intercrossing, diseased, dried and weak branches in old trees to get good sunlight and aeration.  Carry out judicious pruning of the internal branches during August â€“ September, once in three years.  Do not allow flowering upto three years by removing the inflorescences as and when they appear.  Retain two healthy shoots by trimming away the weak shoots among the crowded terminal shoots during August-September annually. Prune back 20 cm of annual growth of the terminals immediately after harvest.
<p style="font-weight:bold;position:relative;top:20px;">Top working of senile orchards for rejuvenation:</p>
Use scions of choice varieties like Alphonso and Banganapalli for top working. Behead the trees to be top worked portion during July- August leaving the main trunk at a convenient height and allow for new shoots to develop.  Adopt cleft method of grafting or softwood grafting on the emerging shoots on the main stem from the cut end during September- October.
<p style="font-weight:bold;position:relative;top:20px;">Growth regulators  </p>
NAA @ 20 ppm is sprayed at flowering to increase the fruit retention.  During February 0.5% Urea (5 g/lit.) or 1% Potassium Nitrate (10g/lit.) may be sprayed to induce flowering, if trees do not flower by that time. Spraying of 2% KNO3 at mustard size will increase the fruit set and retention of fruits.<br>
Application of Paclobutrazol @ 10 g a.i. for non-bearing trees during first fortnight of September will induce flowering and fruitset yield during off years.<br>
<p style="font-weight:bold;position:relative;top:20px;">Plant Protection</p>
<p style="font-weight:bold;position:relative;top:20px;">Pests</p>
<p style="font-weight:bold;position:relative;top:20px;">Hopper</p>
Spraying two rounds of acephate 75 SP@ 1g/lit or phosalone 35 EC  @ 1.5 ml/lit or carbaryl 50 WP 2 g/lit or phosphamidon 40SL 2 ml/lit of water will control hopper. First at the time of panicle emergence and the second two weeks after first spray. Wettable sulphur @ 2 g/lit may be sprayed after spraying carbaryl to avoid mite resurgence. Phosphamidon + neem oil 5 ml/lit of water can be mixed with any insecticides for the control of hopper and shoot webber.
<p style="font-weight:bold;position:relative;top:20px;">Leaf galls and Aphids</p>
Application of Dimethoate or Methyl demeton @ 2 ml/lit will control the pests.
<p style="font-weight:bold;position:relative;top:20px;">Flower Webber </p>
Application of Phosalone 35 EC @ 2 ml/lit will control webber.<br>
<p style="font-weight:bold;position:relative;top:20px;">Nut Weevil<p style="font-weight:bold;position:relative;top:20px;">
Fenthion 100 EC 1ml/lit spray during marble stage and second spray 15 days after the first spray will control nut weevil.<br>
<img src="mango4.jpg" style="height:200px;width:400px;position:relative;left:200px;top:10px;bottom:10px;border-radius:6px">
<p style="font-weight:bold;position:relative;top:20px;">Mealy bug</p>
Spraying of Chlorpyriphos 20 EC 2.5ml/lit or Monocrotophos 36 WSC 1.5ml/lit will give control over the pest. Band the trees with 20 cm wide 400 gauge polythene sheets will prevent the spread of the pest. Similarly, release of Australian ladybird beetle, Cryptolaemus montrouzieri @ 10/tree will be a very effective bio-control measure.<br>
<img src="mango5.jpg" style="height:250px;width:350px;position:relative;left:250px;top:10px;bottom:10px;border-radius:6px">
<p style="font-weight:bold;position:relative;top:20px;">Stem borer</p>
Monocrotophos (36 WSC) 10 ml is soaked in absorbent cotton and placed on the affected stem by removing the bark of 2.5 cmÂ². Then the portion is  wrapped with gunny or plastic papers thereby the chemical gets into the system so as to kill the stem borer. The trees should not be treated during their bearing stage. Application of carbofuran 3 G @ 5g per bore hole and plugging with mud after mechanically removing or killing the grub by introducing a needle or wire will also control the pest.<br>
<p style="font-weight:bold;position:relative;top:20px;">Fruit fly</p>
Spraying of Fenthion 2 ml/lit or malathion 2 ml/lit will control the pest.  Ploughing the inter spaces will expose the pupae. Pheromone trap with methyl eugenol 1 ml in 1 litre of water + 1 ml of malathion solution will attract and kill the female insects.  Take 10 ml of this mixture per trap and keep them in 25 different places in one hectare between 6 a.m. and 8 a.m.  Collect and destroy the fallen fruits.
<p style="font-weight:bold;position:relative;top:20px;">Harvest Season</p>
Harvest spreads from March to June.
