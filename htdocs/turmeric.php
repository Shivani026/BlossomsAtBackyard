<p style="font-weight:bold;font-size:18px;">Varieties</p>
CO 1<br>BSR 1<br>Roma<br>Swarna<br>Sudarshana<br>Suguna<br>Sugandham<br>BSR 2<br>Ranga<br>
Rashmi<br>Rajendra Sonia<br>Krishna<br>Suroma<br>Allepy finger turmeric (AFT)<br>IISR Prabha<br>IISR Prathiba<br>IISR Alleppey Supreme and<br>IISR Kedaram. 
<p style="font-weight:bold;font-size:18px;">Soil and climate</p>
A friable well drained red loamy soil in wet or garden lands under tropical conditions is ideal.
<br>It can be grown in regions receiving an annual rainfall of 1500 m
<p style="font-weight:bold;font-size:18px;">Season</p>
May-June is suitable
<p style="font-weight:bold;font-size:18px;">Seed Treatment</p>
Seed rhizomes dipped in phosalone 35 EC 2ml/lit or monocrotophos 36 WSC 1.5 ml/lit. 0.3% Copper oxychloride for 30 min or
<br>Seed treatment with P. fluorescens 10 g/kg and T. viride as 4 g/ Kg
<p style="font-weight:bold;font-size:18px;">Propagation</p>
Mother rhizome & finger rhizomes. Seed rate of finger rhizome - 2000kg/ha.
<p style="font-weight:bold;font-size:18px;">Main Field Preparation:</p>
Main field is ploughed four times with chisel and disc plough each one time and cultivator twice.
<br>Ridges and furrows are formed at spacing of 45 cm (or) raised beds of 120 cm width are formed at an interval of 30 cm and the laterals are placed at the centre of each bed.
<br>The beds are wetted for 8-12 hours through drip irrigation depending upon soil moisture level.
<p style="font-weight:bold;font-size:18px;">Spacing</p>
45 x 15 cm. 25-30 g weight rhizomes are to be dibbled at a depth of 4 cm.
<p style="font-weight:bold;font-size:18px;">Harvest</p>
The plants will start lodging, yellowing and drying on crop maturity.
<br>The rhizomes are dug with spade or digging forks
<p style="font-weight:bold;font-size:18px;">Yield</p>
Fresh rhizomes: 25-30 t/ha<br>Cured rhizomes: 5-6 t/ha
<p style="font-weight:bold;font-size:18px;">Storage of seed rhizomes</p>
Seed rhizomes can be stored in open sand media with a partially closed pandal.
