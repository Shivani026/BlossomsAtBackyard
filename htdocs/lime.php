<hr style="border-width:20px; border-color:#64DC17"><div class="hr-container" style="top:-15px;left:41%"><p>ACID LIME</p></div></hr>
<p style="font-weight:bold;font-size:18px;">Varieties</p>
Local varities, PKM1, Vikram and Rasraj are the popular varieties in acid lime.<br>
<img src="lim1.png" style="height:180px;width:550px;position:relative;left:180px;top:10px;bottom:10px;border-radius:6px">
<p style="font-weight:bold;font-size:18px;">Soil and climate</p>
The crop can be cultivated in both Tropical and subtropical climate. It can be grown up to 1000 m above MSL. Deep well drained loamy soils are best suited for the cultivation.
<p style="font-weight:bold;font-size:18px;">Season</p>
The planting is done from December – February and June – September
<p style="font-weight:bold;font-size:18px;">Planting</p>
Healthy seedlings are planted during June to December at 5 to 6 m spacing in 75 cm x 75 cm x 75 cm pits.
<p style="font-weight:bold;font-size:18px;">Irrigation</p>
Irrigate copiously after planting. After establishment of the crop, irrigation is given at 7 – 10 days interval. Water stagnation should be avoided.
<p style="font-weight:bold;font-size:18px;">Manures and fertilizers per plant</p>
N is applied in two doses during March and October.  FYM, P2O5 and K2O are to be applied in October.Spray Zinc sulphate at the rate of 0.5% (500 g/100 lit of water) thrice in a year (March, July and October) after the emergence of new flushes.<br>
<img src="lim2.png" style="height:100px;width:410px;position:relative;left:180px;top:10px;bottom:10px;border-radius:6px">
<p style="font-weight:bold;font-size:18px;">After cultivation</p>
The branches of main stem up to 45 cm from ground level have to be removed. Green leaves @ 30 kg per tree are applied once in 3 months.
<p style="font-weight:bold;font-size:18px;">Intercropping</p>
Legumes and vegetable crops can be raised during pre-bearing age.
<p style="font-weight:bold;font-size:18px;">Growth regulator</p>
To increase the fruit set, spray 2, 4 – D @ 20 ppm during flowering stage. For fruit retention, spray 2, 4 – D @ 20 ppm or NAA @ 30 ppm after fruit set (marble size).
<p style="font-weight:bold;font-size:18px;">Plant protection<br>Pests<br>Leaf miner</p>
The pest can be controlled by spraying Dichlorvos 76 WSC @ 1 ml/lit or Dimethoate 30 EC @ 2 ml/lit or Monocrotophos 36 WSC @ 1.5 ml/lit or neem seed kernel extract (NSKE) @ 50g/lit or 3 % neem cake extract or neem oil.
<p style="font-weight:bold;font-size:18px;">Leaf caterpillar</p>
When the infestation is moderate to severe, Quinalphos25 EC @ 2ml/lit is sprayed to control to control the pest. The larvae can be hand picked and destroyed.
<p style="font-weight:bold;font-size:18px;">Fruit sucking moth</p>
Tinospora weed host have to be destroyed. Bait with fermented molasses plus Malathion 50 EC at the rate of 1 ml/lit can be used for control. Bag the fruits with polythene bags punctured at the bottom. Apply smoke and set up light traps or food lures (pieces of citrus fruits) for control.
<p style="font-weight:bold;font-size:18px;">Shoot borer</p>
Prune the withered shoots 4 cm below the dried portions and spray Monocrotophos 36 WSC @ 1 ml/lit or Quinalphos25 EC @ 1.5 ml/lit or Carbaryl 50 WP @ 2 g/lit.
<p style="font-weight:bold;font-size:18px;">Stem borer</p>
The branches containing grubs have to be pruned. Plugging the fresh holes with cotton soaked in Monocrotophos solution mixed @ 5 ml/20 ml of water will also control the pest.
<p style="font-weight:bold;font-size:18px;">Fruit fly</p>
<ul>
<li>To control fruit fly, spray Malathion 50 EC @ 1 ml/lit or Fenthion 100 EC @ 1 ml/lit with 1% crude sugar (10 g/lit). Set up bait with Methyl eugenol 0.1% solution mixed with Malathion 50 EC 0.05% between 6 a.m. and 8 a.m.</li>
<li>Use polythene bags fish meal trap containing 5 g of wet fish meal + 1 ml Dichlorvas in cotton. 50 traps are required/ha, fish meal and Dichlorvos soaked cotton have to be renewed once in 20 and 7 days respectively.</li>
<p style="font-weight:bold;font-size:18px;">Mealy bugs</p>
<li>Debark the branches and apply methyl parathion paste.</li>
<li>Use sticky trap on the fruit bearing shoots at a length of 5 cm.</li>
<li>Use Dichlorvos (0.2%) in combination with fish oil resin soap (25g/lit) as spray or for dipping the fruits for two minutes.</li>
<li>Single soil application of Phorate 10G @ 50g per tree around the base at the time of pruning is essential.</li>
<li>Release the predator, Cryptolaemus montrouzieri for controlling the pest.</li>
<li>Mark the mealy bug infested plants early in the season.</li>
<li>Follow ant control methods such as destruction of ant holes, red ant nests and skirting of citrus trees after fruit harvest which prevents the ant migration through side branches.</li>
<li>Release beetles @ 10/tree once the patrolling of the ants on the trunk is stopped.</li>
<li>Make periodic check during the first fortnight and put dried leaf mulch around the tree trunk 20 days after the release of the beetles to facilitate pupation of the full grown grubs</li>
<li>Make one to three releases per annum depending on the mealy bug populations.</li>
</ul>
<p style="font-weight:bold;font-size:18px;">Nematodes</p>
Apply Carbofuran 3 G @ 75 g/tree to control citrus nematodes in severe infestations. Apply 20 g Pseudomonas fluorescens formulation per tree at a depth of 15 cm and 50 cm away from the trunk once in four months. Soil application of Phorate @ 2 g followed by drenching with 1 % of Metalaxyl plus Mancozeb 72 WP @ 50 ml/ cutting/ poly bag/ kg of nursery soil is done for controlling citrus decline
<p style="font-weight:bold;font-size:18px;">Diseases<br>Twig blight</p>
Prune dried twigs and spray 3% Copper oxychloride or 0.1% Carbendazim at monthly intervals to reduce the spread of disease.
<p style="font-weight:bold;font-size:18px;">Scab</p>
Spray 1% Bordeaux mixture to control the disease
<p style="font-weight:bold;font-size:18px;">Harvest</p>
The crop starts bearing from 3rd year after planting.
<p style="font-weight:bold;font-size:18px;">Post harvest treatment</p>
Treating the fruits with 4% wax emulsion followed by pre-packing in 200 gauge polythene bags with 1 % ventilation improves the shelf life for more than 10 days.
<p style="font-weight:bold;font-size:18px;">Yield</p>
The crop yields about 25 t/ha/year.
