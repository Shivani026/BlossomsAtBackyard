<p style="font-weight:bold;font-size:18px;">Papain extraction</p>
Papain has several industrial uses, the important one being in brewing industries. It is used as “meat tenderiser” and in textile and leather “sanforization” processes and drugs.The method of extraction of papain from papaya fruits is simple. The latex should be tapped from immature papaya fruits. Select 75 to 90 days old fruits.  On the selected fruit, give incisions (cut) with a razor blade or stainless steel knife. The cuts should be given from stalk to tip of the fruit. The depth of the cut should not be more than 0.3 cm.  Four such cuts are given spaced equally on the fruit surface. Tap the latex early in the morning and complete the tapping before 10.00 a.m.  Repeat the tapping four times on the same fruit at an interval of three days. The cut should be given on the fruit surface in places not covered by previous cuts. The latex collected from all the trees in a day should be pooled, shade dried in an aluminium pan or tray and passed through a 50 mesh sieve to remove all foreign matter. In large plantations, vacuum driers can be adopted with advantage. Papain produced by artificial heating will have better colour and high quality. Add potassium meta-bi-sulphite (KMS) at 0.5 % for better colour and keeping quality.<br>
The latex should be dried very rapidly at temperatures of 50°  to 55° C.  Stop drying when the dried product comes off as flakes having a porous texture.  Powder the dried papain by means of wooden mallets or in electrically operated granulators and sieve the powder through 10 mesh sieve. Pack the powder in polythene bags in convenient quantities and seal them. Put the sealed bags in a tin container and seal it after evacuating air. Exposure to air deteriorates the quality of papain and vacuum sealing is therefore necessary. For large scale manufacture of papain, vacuum sealing machine and a granulator will be useful.
<br>The green papaya fruits after extraction of papain can be used for pectin manufacture and “tutty – fruity” or they can be allowed to ripen and made into other products. The Co.2 and Co.5 varieties of papaya released by Tamil Nadu Agricultural University, Coimbatore are ideal for papain production. Tamil Nadu Agricultural University, Coimbatore, supplies pure seeds of these varieties.
<p style="font-weight:bold;font-size:18px;">Yield</p>
The yield of crude papain is as follows:<br>
            CO 2  :  600 kg/ha<br>
            CO 5  :  800 Kg/ha<br>
<p style="font-weight:bold;font-size:18px;">Seed Technology<br>Germination improvement</p>
Store the seeds in airtight containers. Soak the seeds in 100ppm GA3 for16 hours or in 2% fresh leaf extract of arappu or 1% pungam leaf extract or pellet the seeds with arappu leaf powder.
<p style="font-weight:bold;font-size:18px;">Optimum depth of sowing: </p>
Sow seeds at 1cm depth for better germination and seedling growth.
<p style="font-weight:bold;font-size:18px;">Grading:</p>
 BSS6 wiremesh sieve.
<p style="font-weight:bold;font-size:18px;">Storage:</p>
Dry seeds to 8-10% moisture and treat with halogen mixture containing CaOCl2, CaCO3 and arappu leaf powder (at 5:4:1 ratio)@3g/kg and pack in cloth bag to maintain viability upto 5 months.
