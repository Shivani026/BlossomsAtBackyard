<hr style="border-width:20px; border-color:#64DC17"><div class="hr-container" style="top:-15px;left:41%"><p>JACK FRUIT</p></div></hr>
<p style="font-weight:bold;font-size:18px;">Varieties</p>
Velipala, Singapore, Hybrid jack, Panruti selection, Thanjavur jack, Burliar 1, PLR 1, PLR (J) 2 and PPI 1 are the popular varieties in jack.<br>
<img src="jac1.png" style="height:200px;width:450px;position:relative;left:200px;top:10px;bottom:10px;border-radius:6px">
<p style="font-weight:bold;font-size:18px;">Soil and climate</p>
Deep well drained soil is necessary for jack cultivation. Soil pH around 5.5 at the time of planting is desirable. Otherwise treat the soil with 1% Aluminium sulphate in the pit to reduce the pH. The crop comes up well in the plains and up to an elevation of 1200 m.
<p style="font-weight:bold;font-size:18px;">Propagation</p>
Soft wood grafting: Large scale propagation of jack can be done by cleft grafting during July-August on 4 month old seedling rootstock
<p style="font-weight:bold;font-size:18px;">Preparation of field and planting</p>
Dig pits of 1 m x 1 m x 1 m. Fill up the pits with top soil mixed with 10 Kg of FYM and 1 Kg of neem cake per pit. Plant preferably grafts during June – December at 8 x 8 m spacing.
<p style="font-weight:bold;font-size:18px;">Irrigation</p>
Once in a week till the plant gets established. Thereafter irrigation is given as and when necessary.
<p style="font-weight:bold;font-size:18px;">Manures and fertilizers</p>
The manures and fertilizers should be applied in two splits during May - June and September - October.<br>
<img src="jac2.png" style="height:200px;width:450px;position:relative;left:200px;top:10px;bottom:10px;border-radius:6px">
<p style="font-weight:bold;font-size:18px;">Plant protection<br>PestFruit borer:</p>
Apply Carbaryl 50 WP @ 2 g/lit.<br>
(a) jackfruit severely infested with jackfruit fruit borer (b) immature larvae on the fruit surface (c) larvae inside the tunnel in infested fruit tissues
<p style="font-weight:bold;font-size:18px;">Spittle bug</p>
Spray methyl parathion 50 EC 2ml/lit or Methyl demeton 25 EC @ 2ml/lit. or Phosphamidon 40 SL 2 ml/lit or dust Methyl parathion 2 D or Quinalphos dust 1.5 D. 
<p style="font-weight:bold;font-size:18px;">Disease<br>Rhizopus rot</p>
Spray 1 % Bordeaux mixture or Copper oxychloride 2.5 g/lit. Three sprays must be given at 15 days interval.
<p style="font-weight:bold;font-size:18px;">Harvest</p>
The yield commences from 5th year in grafts and 8th year in seedling trees.  Harvest is done during March-July.
<p style="font-weight:bold;font-size:18px;">Yield</p>
The crop yields about 30-40 t/ha
