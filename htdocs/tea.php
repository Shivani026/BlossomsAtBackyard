<p style="font-weight:bold;font-size:18px;">Varieties</p>
Pandian, Sundaram, Golconda, Jayaram, Evergreen, Athrey, Brookeland, BSS 1, BSS 2, BSS 3, BSS 4, BSS 5, Biclonal seed stocks and Grafts.<br>
<img src="tea1.png" style="height:180px;width:450px;position:relative;left:200px;top:10px;bottom:10px;border-radius:6px">
<img src="tea2.png" style="height:400px;width:500px;position:relative;left:180px;top:20px;bottom:10px;border-radius:6px">
<p style="font-weight:bold;font-size:18px;">Soil and climate</p>
Tea requires well drained soil with high amount of organic matter and pH 4.5 to 5.5. The performance of tea is excellent at elevations ranging from 1000 - 2500 m.Optimum temperature: 20 - 270 C.
<p style="font-weight:bold;font-size:18px;">Nursery</p>
The nursery soil should be well drained and deep loam in nature with pH of 4.5 to 4.8. The soil and sand used in the preparation of rooting medium should be tested for pH and nematode infestation.<br>
<img src="tea3.png" style="height:180px;width:450px;position:relative;left:200px;top:10px;bottom:10px;border-radius:6px">
<p style="font-weight:bold;font-size:18px;">Pre-treatment of rooting medium</p>
Treating with Aluminium sulphate can reduce soil pH. For this purpose the nursery soil is formed into beds of one metre width and about 8 cm height and of a convenient length. Then the beds are drenched with 2% solution of Aluminium sulphate applied at 10 litres/2.5 sq.m of area. Over this another layer of soil of 8 cm height is spread and again drenched with equal quantity of water twice.  Then the soil is allowed to dry and the pH is checked before use in the nursery.
<p style="font-weight:bold;font-size:18px;">Preparation of sleeves</p>
Polythene sleeves of 150 or 200 gauge and 10 cm width and 30 - 45 cm length may be used. Drainage holes may be provided at the bottom. The lower 3/4 of the sleeves should be filled with 1:3 sand and soil mixture and the top 1/4 with 1:1 sand and soil mixture and staked in rows. Overhead shade is provided.<br>
<img src="tea4.png" style="height:180px;width:450px;position:relative;left:200px;top:10px;bottom:10px;border-radius:6px">
<p style="font-weight:bold;font-size:18px;">Selection of mother bush and its treatment</p>
Healthy and vigorously growing high yielding bushes should be selected.  Apply to each selected bush with 40 g of young tea mixture + 60:90 NK mixture up to 5 years. The following mixture has to applied before taking the cuttings<br>
0.5 % AlSO4 + 1 % MgSO4 (before 3 weeks)<br>
2 % Zn SO4 (before 2 weeks)<br>
1 % Urea (before 1 week)
<p style="font-weight:bold;font-size:18px;">Preparation of cuttings</p>
Cuttings are taken on April - May and August - September. Semi hard-wood cuttings are prepared with one leaf and an internode with a slanting cut at the bottom.
<p style="font-weight:bold;font-size:18px;">Planting of cuttings</p>
The sleeves are watered thoroughly and holes are made in the soil. The cuttings are inserted in the hole and the soil around is pressed firmly to avoid airspace followed by watering. Small polythene tents may be provided which maintain high humidity and regulate the temperature inside. Cuttings may take 10 - 12 weeks for rooting. After 90 days i.e. when all the cuttings have rooted, the polythene tent may be removed gradually over a period of 10 - 15 days.
<p style="font-weight:bold;font-size:18px;">Manuring of nursery</p>
After the tent is removed the cuttings are sorted and staked. 30 g of Nursery soluble mixture of the following composition dissolved in 10 litres of water may be applied over an area of 4 sq.m. This should be done fortnightly.
<p style="font-weight:bold;font-size:18px;">Hardening of the cuttings</p>
Hardening of 4 - 6 months old young cuttings should be done by removing shade gradually in stages over a period of 4 - 6 weeks starting from a few hours exposure to sun every day initially and extending the time of exposure gradually.
<p style="font-weight:bold;font-size:18px;">Methods of planting<br>Single Hedge System</p>
In this method, the spacing adopted is 1.20 x 0.75 m accommodating 10,800 plants/ha.
<p style="font-weight:bold;font-size:18px;">Season and planting</p>
May - June or September - October
Sleeves should be opened lengthwise without injuring the roots and planted in the pit and the soil is gently pressed.
<p style="font-weight:bold;font-size:18px;">Irrigation</p>
Subsoil irrigation may be given for young tea seedlings during summer months.
<p style="font-weight:bold;font-size:18px;">Manuring</p>
Manuring should be done 2 months after planting. Phosphorous should be applied at 80 - 100 kg/ha as Rock phosphate once in a year by placement at 15 - 25 cm depth up to the first pruning and thereafter once in two years.  N : K ratio 2 : 3 should be adapted for the first 3 years and a ratio 1 : 1 thereafter.<br>
