<hr style="border-width:20px; border-color:#F00E1F"><div class="hr-container" style="top:-15px;left:41%"><p>FENUGREEK</p></div></hr>
<img src="fen1.png" style="height:200px;width:450px;position:relative;left:220px;top:10px;bottom:10px;border-radius:6px">
<p style="font-weight:bold;font-size:18px;">Varieties</p>
Co 1,Pusa Early Bunching,Lam selection 1,Rajendra Kranti,Kissar Sonali,RMT1,CO 2
<p style="font-weight:bold;font-size:18px;">Soil:</p>
A rich well drained loamy soil is best suited for fenugreek cultivation.
<p style="font-weight:bold;font-size:18px;">Climate</p>
Cool and comparatively dry, frost free climate
<p style="font-weight:bold;font-size:18px;">Season:</p>
 June - July and October - November
<p style="font-weight:bold;font-size:18px;">Seed rate: </p>
12 kg/ha is required
<p style="font-weight:bold;font-size:18px;">Seed treatment: </p>
Treated with Azospirillum 1.5 kg/ha + Trichoderma viride @ 50 g/ha for 12 kg of seeds.
<p style="font-weight:bold;font-size:18px;">Field preparation and sowing:</p>
Prepare the main field to a fine tilth.<br>
Add FYM 20 - 25 t/ha before last ploughing. Form beds and channels of 3.5 x 1.5 m.<br>
Seeds are sown at a spacing of 20 X 15 cm. Spray pre-emergence herbicide Fluchloralin 700 ml in 500 lit of water per ha.
<p style="font-weight:bold;font-size:18px;">Manuring:</p>
Basal- FYM 20 - 25 t/ha and N, P, K at 30:25:40 kg/ha.<br>
Top dressing- 20 kg of N at 30 days after sowing.
<p style="font-weight:bold;font-size:18px;">Irrigation</p>
First irrigation is given immediately after sowing, second on the third day and subsequently at 7 - 10 days intervals.
<p style="font-weight:bold;font-size:18px;">Aftercultivation</p>
Plants are thinned at 20 - 25 days after sowing and the thinned seedlings are used as greens. One pinching at a height of about 4” will encourage branching.Weeding is done as and when necessary.
<p style="font-weight:bold;font-size:18px;">Plant protection:</p>
Diseases<br>
Root rot:<br>
Root rot can be controlled by soil application of Neem cake @ 150 g/ha and seed treatment with Trichoderma viride @ 4g/kg  of seed and drenching with Carbendazim 0.5 g/l or Copper oxychloride 2 g/litre.
<p style="font-weight:bold;font-size:18px;">Powdery mildew:</p>
Dust Sulphur at 25 kg/ha or foliar spray with wettable sulphur 2 g/lit at the time of appearance of disease.
<p style="font-weight:bold;font-size:18px;">Yield:</p>
The yield of green   :  4000 - 5000 kg/ha<br>
Grains                    :  500 - 700 kg/ha
